package game.portableItems;

/** An Enum class was created to indicate the Capabilities of EggType
 * Since the capabilities are known(not going to change), enum is used.
 * EggType can be divided into:
 * STEGOSAUR  : egg of Stegosaur
 * BRACHIOSAUR: egg of Brachiosaur
 * ALLOSAUR   : egg of Allosaur
 */
public enum EggType {
    STEGOSAUR, BRACHIOSAUR,ALLOSAUR;
}
